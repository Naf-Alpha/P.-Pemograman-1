x = input("Masukkan nilai x : ")
y = input("Masukkan nilai y : ")

jumlah = int(x) + int(y)
print("Hasil dari x + y = "+str(jumlah))

kurang = int(x) - int(y)
print("Hasil dari x - y = "+str(kurang))

kali = int(x) * int(y)
print("Hasil dari x * y = "+str(kali))

bagi = int(x) / int(y)
print("Hasil dari x / y = "+str(bagi))

modulus = int(x) % int(y)
print("Hasil dari x % y = "+str(modulus))